public class Personagem {
    private int dano;
    private int resistencia;
    private int velodidadeMovimento;
    private double alcanceAtaque;

    public int getDano() {
        return dano;
    }

    public void setDano(int dano) {
        this.dano = dano;
    }

    public int getResistencia() {
        return resistencia;
    }

    public void setResistencia(int resistencia) {
        this.resistencia = resistencia;
    }

    public int getVelodidadeMovimento() {
        return velodidadeMovimento;
    }

    public void setVelodidadeMovimento(int velodidadeMovimento) {
        this.velodidadeMovimento = velodidadeMovimento;
    }

    public double getAlcanceAtaque() {
        return alcanceAtaque;
    }

    public void setAlcanceAtaque(double alcanceAtaque) {
        this.alcanceAtaque = alcanceAtaque;
    }
    
}
